    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {

        model.beans.Configuration c = null;

        try {

            c = new model.beans.Configuration();
            c.setProjectName(jTextFieldProjectName.getText());
            c.setHelpPath(jTextFieldURLHelp.getText());
            c.setJWSPath(jTextFieldURLSSN.getText());
            c.setJmsTimeWaitConnectivity(Integer.parseInt(jTextFieldTWC.getText()) * 1000);
            c.setJmsTimeWaitMessage(Integer.parseInt(jTextFieldTWM.getText()) * 1000);
            c.setSecondsLive(jTextFieldLiveUpdate.getText());
            c.setUsedMasterShaper(jTextFieldMasterShaper.getText());
            c.setMasterShaperServer(jTextFieldMasterShaperServer.getText());

            new controllers.ControllerConfiguration().truncateConfiguration();
            new controllers.ControllerConfiguration().saveConfiguration(c);

            jLabelMessage.setText("Done !!");
            
        } catch (Exception e) {
            notifications.error("error saving the new configuration", e);
        }
    }
