    private void onLoad() {

        try {

            this.configuration = new ControllerConfiguration().getInitialConfiguration();
            jTextFieldProjectName.setText(configuration.getProjectName());
            jTextFieldTWC.setText(configuration.getJmsTimeWaitConnectivity() / 1000 + "");
            jTextFieldTWM.setText(configuration.getJmsTimeWaitMessage() / 1000 + "");
            jTextFieldURLHelp.setText(configuration.getHelpPath());
            jTextFieldURLSSN.setText(configuration.getJWSPath());
            jTextFieldLiveUpdate.setText(configuration.getSecondsLive());
            jTextFieldMasterShaper.setText(configuration.getUsedMasterShaper());
            jTextFieldMasterShaperServer.setText(configuration.getMasterShaperServer());
            
            // the user has a privileges
            if (User.getInstance().getLevel() == 2) {
                jButtonSave.setEnabled(false);
                jButtonSave.setVisible(false);
                jLabelMessage.setText("you don't have privileges for change these settings");
            }

        } catch (Exception e) {
            notifications.error("error ", e);
        }

    }

